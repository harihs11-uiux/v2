function Textfield({ value, onChange, onHover }: { 
  value?: string; 
  onChange?: (value: string) => void; 
  onHover?: (isHovered: boolean) => void; 
}) {
  const [isHovered, setIsHovered] = React.useState(false);
  const [isFocused, setIsFocused] = React.useState(false);
  const [inputValue, setInputValue] = React.useState(value || "FLYJAC LOGISTICS PVT LTD");

  const showBorder = isHovered || isFocused;

  const handleMouseEnter = () => {
    setIsHovered(true);
    onHover?.(true);
  };

  const handleMouseLeave = () => {
    setIsHovered(false);
    onHover?.(false);
  };

  const handleChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    const newValue = e.target.value;
    setInputValue(newValue);
    onChange?.(newValue);
  };

  return (
    <div
      className="basis-0 grow min-h-px min-w-px relative shrink-0"
      data-name="Textfield"
    >
      <div className="relative size-full">
        {/* Blue border overlay on hover/focus */}
        {showBorder && (
          <div className="absolute border border-[#3874ff] border-solid inset-0 pointer-events-none z-10" />
        )}
        
        <div 
          className="box-border content-stretch flex flex-row gap-1 items-start justify-start px-2 py-1.5 relative w-full cursor-text"
          onMouseEnter={handleMouseEnter}
          onMouseLeave={handleMouseLeave}
        >
          <div className="basis-0 grow leading-[0] min-h-px min-w-px relative shrink-0 text-[#050e25] text-[0px] text-left">
            <input
              type="text"
              value={inputValue}
              onChange={handleChange}
              onFocus={() => setIsFocused(true)}
              onBlur={() => setIsFocused(false)}
              className="w-full bg-transparent border-none outline-none font-['Inter:Regular',_sans-serif] font-normal text-[14px] leading-[18px] text-[#050e25] placeholder:text-[#9497a1]"
              style={{ fontSize: '14px', lineHeight: '18px' }}
            />
            <span className="css-po3kg2 text-[#cf3b3b] text-[7.74px] absolute right-0 top-0">●</span>
          </div>
        </div>
      </div>
    </div>
  );
}

